-- Supabase 또는 PostgreSQL 환경에서 실행하세요.

CREATE TABLE IF NOT EXISTS festivals (
  id bigint GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  name text NOT NULL,
  region text NOT NULL,
  month text NOT NULL,
  description text,
  created_at timestamptz NOT NULL DEFAULT now()
);

